#!/usr/bin/env node
// render-framework.mjs — turn framework.config.json into edge vhosts + DNS + cert SANs.
// Plan 0004: the TLD is one variable; everything below derives from it. Change the
// `tld` in framework.config.json, re-run this, and the whole edge/DNS/cert set follows.
//
//   node scripts/render-framework.mjs                 # render from ./framework.config.json
//   node scripts/render-framework.mjs path/to/cfg.json out-dir
//
// Writes (default ./generated/):
//   generated/nginx/<host>.conf   one vhost per public-facing cassette
//   generated/dns.txt             the DNS record set (edge A + CNAMEs)
//   generated/cert-sans.txt       the SAN list for the active TLD
// It does NOT touch the live edge — that's Plan 0004 Phase 2.

import { readFileSync, writeFileSync, mkdirSync, rmSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const cfgPath = process.argv[2] || join(root, "framework.config.json");
const outDir = process.argv[3] || join(root, "generated");

const cfg = JSON.parse(readFileSync(cfgPath, "utf8"));
const { tld, edge_ip, ca_cert, le_cert_dir, cassettes } = cfg;
const host = (name) => `${name}.${tld}`;

function vhost(name, c) {
  const h = host(name);
  const mtls =
    c.tier === "mtls"
      ? `    # mTLS — family device cert required (Plan 0004 tier: mtls)\n` +
        `    ssl_client_certificate ${ca_cert};\n` +
        `    ssl_verify_client on;\n`
      : "";
  const proxySsl = c.proxy_ssl
    ? `        proxy_ssl_server_name on;\n        proxy_ssl_name ${h};\n        proxy_ssl_verify off;\n`
    : "";
  return `# GENERATED by render-framework.mjs from framework.config.json — DO NOT EDIT BY HAND
# cassette: ${name}   tier: ${c.tier}${c.note ? `   (${c.note})` : ""}
server {
    listen 80;
    server_name ${h};
    location /.well-known/acme-challenge/ { root /var/www/letsencrypt; }
    location / { return 301 https://$host$request_uri; }
}
server {
    listen 443 ssl http2;
    server_name ${h};
    ssl_certificate     ${le_cert_dir}/${h}/fullchain.pem;
    ssl_certificate_key ${le_cert_dir}/${h}/privkey.pem;
${mtls}    location / {
        proxy_pass ${c.upstream};
${proxySsl}        proxy_set_header Host ${h};
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
        proxy_read_timeout 300s;
        proxy_send_timeout 300s;
    }
}
`;
}

rmSync(outDir, { recursive: true, force: true });
mkdirSync(join(outDir, "nginx"), { recursive: true });

const dns = [`; DNS for ${tld} — generated from framework.config.json`, `edge\t600\tIN\tA\t${edge_ip}`];
const sans = [];
const summary = [];

for (const [name, c] of Object.entries(cassettes)) {
  if (c.tier === "wireguard") {
    summary.push(`  ${host(name).padEnd(34)} wireguard  → NO public vhost, NO public DNS (mesh only)`);
    continue;
  }
  writeFileSync(join(outDir, "nginx", `${host(name)}.conf`), vhost(name, c));
  dns.push(`${name}\t600\tIN\tCNAME\tedge.${tld}.`);
  sans.push(host(name));
  summary.push(`  ${host(name).padEnd(34)} ${c.tier.padEnd(7)}    → vhost + CNAME + cert SAN`);
}

writeFileSync(join(outDir, "dns.txt"), dns.join("\n") + "\n");
writeFileSync(join(outDir, "cert-sans.txt"), sans.join("\n") + "\n");

console.log(`Rendered for TLD = ${tld}\n`);
console.log(summary.join("\n"));
console.log(`\n  → ${sans.length} public vhosts + DNS + cert SANs written to ${outDir}/`);
console.log(`  Change "tld" in framework.config.json and re-run to repoint the entire edge.`);
