const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const DATA_PATH = path.join(ROOT, 'hardware', 'imac-hardware-data.json');
const MD_PATH = path.join(ROOT, 'hardware', 'imac-legacy-max-out-hardware.md');

const data = JSON.parse(fs.readFileSync(DATA_PATH, 'utf8'));

const byMachine = {
  '2012 iMac': data.filter(r => r.machine === '2012 iMac'),
  '2017 iMac': data.filter(r => r.machine === '2017 iMac')
};

function esc(s) {
  return s ? String(s).replace(/\|/g, '\\|') : '—';
}

function makeTable(rows) {
  if (!rows || rows.length === 0) return '_No data._\n';
  
  const header = '| Category | Specification | Max / Recommended | Upgrade Path | Recommended Part | Approx. Price (Florida) | Status | Notes |\n' +
                 '|----------|---------------|-------------------|--------------|------------------|-------------------------|--------|-------|\n';
  
  const body = rows.map(r => {
    return `| ${esc(r.category)} | ${esc(r.specification)} | ${esc(r.maxRecommended)} | ${esc(r.upgradePath)} | ${esc(r.recommendedPart)} | ${esc(r.price)} | ${esc(r.status)} | ${esc(r.notes)} |`;
  }).join('\n');
  
  return header + body + '\n';
}

